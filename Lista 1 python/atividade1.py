#A imobiliária Imóbilis vende apenas terrenos retangulares. Faça um algoritmo para ler as dimensões de um terreno e exibir a área do terreno.
comprimento = float (input("Insira o comprimento do terreno retangular: "))
largura = float (input("Insira a altura do terreno retangular: "))
area = comprimento * largura
print("A área do terreno retangular é:",area)