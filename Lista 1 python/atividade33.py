#Faça um algoritmo que calcule e mostre a área de um trapézio. 
#Sabe-se que: A = (base maior + base menor)* altura)/2 ;
basemaior = float (input("Insira a medida da base maior do trapézio:"))
basemenor = float (input("Insira a medida da base menor do trapézio:"))
altura = float (input("Insira a medida da altura do trapézio:"))
area = (basemaior + basemenor) * altura / 2
print ("A área do trapézio é de:",area)
