#Faça um algoritmo que receba três números, calcule e mostre a multiplicação desses números.
numero1 = float (input("Digite o primeiro número:"))
numero2 = float (input("Digite o segundo número:"))
numero3 = float (input("Digite o terceiro número:"))
total = numero1 * numero2 * numero3
print (f"O total da multiplicação dos números {numero1} x {numero2} x {numero3} é de {total}")
